function mrwquadrature(n::Int)
    rules = [15, 20, 25, 30]
    if n >= 3 && n <= 10
        return Float64.(gqlogx[n-2]), Float64.(gqlogw[n-2])
    else
        if n in rules
            gqlogx[8+Int((n-10)/5)], gqlogw[8+Int((n-10)/5)] 
        else
            throw(GQExceptionDegree())
        end
    end
end